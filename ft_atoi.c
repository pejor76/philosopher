#include "phil.h"

int	ft_atoi(const char *str)
{
	int	x;
	int	i;
	int	num;

	i = 0;
	num = 0;
	x = 1;
	while ((str[i] >= '\t' && str[i] <= '\r') || str[i] == ' ')
		i++;
	if (str[i] == '-')
	{
		x = x * -1;
		i++;
	}
	else if (str[i] == '+')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		num = num * 10 + (str[i] - 48);
		i++;
	}
	return (num * x);
}
